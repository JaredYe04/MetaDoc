/**
 * ============================================================================
 * 🧠 San 值（Sanity Value）代码质量理念
 * ============================================================================
 *
 * "San 值" = 代码的可维护性和健康度
 *
 * Linting 不是为了阻止你提交代码，而是为了维护代码的 "san 值"。
 *
 * ┌─────────────────────────────────────────────────────────────────────────┐
 * │  做法              │  结果                           │  评价            │
 * ├─────────────────────────────────────────────────────────────────────────┤
 * │  ❌ 禁用检查       │  问题依然存在，只是你看不到     │  掩耳盗铃        │
 * │  ❌ 绕过检查       │  技术债务累积，后期成本更高     │  饮鸩止渴        │
 * │  ✅ 修复问题       │  代码保持健康，团队效率提升     │  负责任的做法    │
 * └─────────────────────────────────────────────────────────────────────────┘
 *
 * ----------------------------------------------------------------------------
 * 规则层级体系（三层分级）
 * ----------------------------------------------------------------------------
 *
 * 1️⃣ error（致命）—— 会导致运行时错误或逻辑错误
 *    - 例如：no-undef（使用未定义变量 → ReferenceError）
 *    - 例如：no-dupe-else-if（重复条件 → 逻辑错误）
 *    - ✅ 必须立即修复，零容忍
 *
 * 2️⃣ warn（警告）—— 代码质量问题
 *    - 例如：no-unused-vars（未使用变量 → 死代码）
 *    - 例如：no-case-declarations（case 块中声明变量 → 作用域陷阱）
 *    - 📋 应该逐步修复，有计划地消除
 *
 * 3️⃣ off（暂缓）—— 暂时关闭但有恢复计划
 *    - 通常是数量太多暂时无法全部修复
 *    - 📅 有计划地逐步恢复，不永久关闭
 *
 * ----------------------------------------------------------------------------
 * 我们的承诺
 * ----------------------------------------------------------------------------
 *
 * ✅ 不禁用检查来掩盖问题
 * ✅ 不删除测试来让构建通过
 * ✅ 修复问题的根本原因
 * ✅ 将修复代码视为与编写代码同等重要的工作
 *
 * 📖 完整文档请参阅：AGENTS.md（第26-64行）
 * ============================================================================
 */

require('@rushstack/eslint-patch/modern-module-resolution')

module.exports = {
  extends: [
    'eslint:recommended',
    'plugin:vue/vue3-recommended',
    '@electron-toolkit',
    '@vue/eslint-config-prettier'
  ],
  parser: 'vue-eslint-parser',
  parserOptions: {
    parser: '@typescript-eslint/parser'
  },
  plugins: ['@typescript-eslint'],
  globals: {
    NodeJS: 'readonly',
    Electron: 'readonly',
    ChildNode: 'readonly'
  },
  rules: {
    // =========================================================================
    // 第 1 层：off（暂缓）— 暂时关闭但有计划恢复的规则
    // =========================================================================
    // 这些规则被暂时禁用是因为当前代码库中违规数量太多，需要分阶段修复。
    // 这不是永久关闭，而是 "饮鸩止渴" 之前的临时措施。
    // TODO: 制定计划逐步修复并恢复这些规则

    'no-unused-vars': 'off', // 未使用变量 — 死代码问题，影响可读性
    'vue/require-default-prop': 'off', // Vue props 默认值 — 组件健壮性
    'vue/multi-word-component-names': 'off', // 多单词组件名 — 避免 HTML 冲突
    '@typescript-eslint/no-var-requires': 'off', // 使用 require() 替代 import

    // =========================================================================
    // 第 2 层：warn（警告）— 代码质量问题，应该逐步修复
    // =========================================================================
    // 这些规则标记的是代码质量问题，不会导致立即的运行时错误，但会积累
    // 技术债务。分为三个风险等级逐步修复。

    // --- 2.1 低风险警告（保持 warn，可延后处理）---
    // 这些问题影响较小，可以在有余力时修复
    'no-useless-escape': 'warn', // 无用的转义字符
    'no-control-regex': 'warn', // 控制字符正则表达式
    'require-yield': 'warn', // generator 函数缺少 yield

    // --- 2.2 中等风险警告（建议近期修复）---
    // 这些问题可能导致意外的行为或作用域问题
    'no-case-declarations': 'warn', // case 块中声明变量 — 作用域陷阱
    'no-unreachable': 'warn', // 不可达代码 — 逻辑错误或死代码

    // --- 2.3 高风险警告（优先修复，修复后升级为 error）---
    // 这些问题可能导致难以发现的 bug 或运行时错误
    'no-constant-condition': 'warn', // 恒真/恒假条件 — 逻辑错误
    'no-prototype-builtins': 'warn', // 直接调用 Object.prototype 方法 — 安全风险
    'no-redeclare': 'warn', // 重复声明变量 — 作用域混淆

    // =========================================================================
    // 第 3 层：error（致命）— 会导致运行时错误或逻辑错误，必须修复
    // =========================================================================
    // 这些规则标记的是致命问题，会导致程序崩溃或严重逻辑错误。
    // 零容忍，发现即修复，绝不妥协。

    'no-undef': 'error', // 使用未定义变量 → ReferenceError（运行时崩溃）
    'no-empty': 'error', // 空代码块 → 可能遗漏逻辑或错误处理（逻辑缺陷）
    'no-dupe-else-if': 'error' // 重复条件 → 某些分支永远不会执行（不可达代码）
  },
  overrides: [
    {
      files: ['src/renderer/src/plugins/**/*.{ts,js}'],
      rules: {
        'no-restricted-imports': [
          'error',
          {
            patterns: [
              {
                group: ['**/stores/workspace', '@renderer/stores/workspace'],
                message: 'Plugins must use MetaDocHost API instead of importing workspace store directly.'
              }
            ]
          }
        ]
      }
    }
  ]
}
